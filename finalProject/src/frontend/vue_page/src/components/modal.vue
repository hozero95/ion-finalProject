          이용할 수 있도록 ”판매회원”의 개인정보(신용정보 포함)보호를 위한
          기술적, 관리적 조치를 다합니다. ③ “회사”는 컴퓨터 등 정보통신설비의
          보수, 점검, 교체 및 고장, 통신 두절 등의 사유가 발생하는 경우 서비스의
          제공을 일시적으로 중단할 수 있으며, 이와 관련하여 고의 또는 중대한
          과실이 없는 한 책임을 지지 않습니다. ④ “회사”는 ”판매회원”이 제공 또는
          게시한 정보, 자료, 사실의 신뢰도, 정확성 등 내용에 대하여 관련 법령에
          따라 “회사"가 책임을 부담하는 경우를 제외하고는 어떠한 보증이나 책임도
          부담하지 않습니다. 제16조 (개인정보의 보호) ① ”판매회원”은 서비스를
          이용하는 과정에서 지득한 “고객” 등 타인의 개인정보를 제공 받은 목적
          이외의 용도로 사용할 수 없으며, 이를 위반할 경우 관련 법령에 의한 모든
          민ㆍ형사상의 법적 책임을 지고 자신의 노력과 비용으로 즉시 “회사”를
          면책시키고 문제를 해결해야 합니다. ② “회사”가 개인정보의 보호를 위하여
          상당한 주의를 기울였음에도 불구하고, 특정 ”판매회원”이 본 조를
          위반하여 타인의 개인정보를 유출 또는 유용한 경우 “회사”는 그에 대하여
          아무런 책임을 지지 않습니다. ③ “회사”는 개인정보 보호를 위하여 배송
          등의 목적으로 ”판매회원”에게 공개되어 있는 “고객”의 개인정보를 상당
          기간이 경과한 후 비공개 조치할 수 있습니다. ④ “회사”는 전기통신사업법
          등 관련 법령이 규정하는 적법한 절차에 따라 수사관서 등이 “회사”에게
          ”판매회원”에 관한 정보의 제공을 요청한 경우, 관련 자료를 제출할 수
          있습니다. 제17조 (비밀유지) “회사”와 ”판매회원”은 관련 법령에 의하거나
          경찰, 검찰, 법원, 기타 수사 또는 조사를 담당하는 정부기관 등으로부터
          요구 받는 경우를 제외하고는 서비스 제공 및 이용 과정에서
          상대방으로부터 제공받거나 취득한 고객 명부, 기술정보, 생산 및
          판매계획, 노하우 기타 비밀로 관리되는 정보를 제3자에게 제공하거나
          유출하여서는 안 되며, 그러한 정보를 이용계약의 이행 이외의 목적으로
          이용하여서는 안 됩니다. 제18조 (금지행위) ”판매회원”은 다음과 같은
          행위를 하여서는 안 됩니다. 1. 자신 또는 제3자의 ID를 이용하여 직접
          사용을 위한 의사 없이(직접 사용의 증거를 제시하지 못한 경우를 포함함)
          재판매 목적 또는 쿠폰 할인, 환불 정책, 고객 보상 정책, 판매량 표시
          증가 등을 이용한 부당한 이익 취득을 위한 목적으로 “사이트”에서 자신
          또는 제3자의 상품을 반복적으로 또는 대량으로 구매하거나 구매한 것처럼
          보이도록 하는 행위, 그러한 행위를 교사 또는 방조하는 행위, 기타
          단독으로 또는 제3자와 공모하여 “서비스”를 부정하게 또는 비정상적으로
          이용하여 자신 또는 제3자가 부당한 이득을 취하게 하고 “회사” 또는
          타인에게 손해 또는 손실을 입히는 행위 2. “사이트”에서 판매 중인 상품에
          대하여 “고객”과 “사이트” 또는 “서비스”를 배제하는 형태의 거래(이하
          “직거래”라 합니다)를 하거나, “고객”에게 “직거래”를 제안하거나,
          “고객”의 그러한 제안을 수락하는 행위 3. 상품의 중요 정보(원산지,
          유통기한, 인허가사항 등)를 오기재 또는 누락하거나, 상품과 관련 없는
          카테고리에 상품을 등록하거나, 상품과 관련 없는 키워드를 상품명 또는
          상품정보에 등록하는 행위 4. 동일한 상품(동일한 상품이나 상품명만 다른
