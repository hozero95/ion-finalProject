          알려야 하며, ”판매회원”이 이를 해태하여 입은 불이익에 대하여 “회사”는
          일체의 책임을 부담하지 않습니다. ⑦ ”판매회원”은 상품 등에 관한 “고객”
          및 “회사”의 문의에 지체없이 정확하게 답변하여야 하며, 허위 또는
          부정확한 답변으로 “고객” 및 “회사”에 손해가 발생하는 경우 ”판매회원”의
          책임 및 비용으로 해결하여야 합니다. 제15조 (“회사”의 권리 및 의무) ①
          “회사”는 ”판매회원”의 회원가입 신청 시 해당 정보 및 서류를 심사하고 그
